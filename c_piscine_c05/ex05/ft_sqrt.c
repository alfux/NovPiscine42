int	ft_sqrt(int nb)
{
	int	x;	

	x = 46340;
	while (x > 0 && x < 46341)
	{
		if (x * x == nb)
			return (x);
		else if (x * x > nb)
			x /= 2;
		else
			if (!x++ || x * x > nb)
				break ;
	}
	return (0);
}
