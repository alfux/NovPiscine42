#include <unistd.h>

void	ft_putnbr(int nb)
{
	char			digit;

	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else if (nb < 0)
	{
		nb *= -1;
		write(1, "-", 1);
		ft_putnbr(nb);
	}
	else
	{
		digit = nb % 10 + 48;
		if (nb > 9)
			ft_putnbr(nb / 10);
		write(1, &digit, 1);
	}
}
